//   Enemy heavy      -> color index 20 (grey)
//   Enemy diver      -> color index 22 (green)
//   Enemy bomber     -> color index 24 (white/bright)
//   Boss             -> color index 26
//   Shot player      -> color index 28 (yellow)
//   Shot enemy       -> color index 29 (red)
//   Explosion        -> color index 30
// ============================================================================

