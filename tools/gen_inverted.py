"""Generate inverted (black) variants of 2-bitplane enemy sprites for PF2."""
import sys

def invert_2bpl(mask, dataHi, dataLo, count):
    """Invert polarity: 1<->2, 0/3 stay the same.
    PF2 2-bitplane: 0=transparent, 1=white (slot 9), 2=black (slot 10), 3=light grey (slot 11).
    Swapping 1<->2 makes the body black and the outline white (true black enemy)."""
    invDataHi = []
    invDataLo = []
    for i in range(count):
        m = mask[i]
        h = dataHi[i]
        l = dataLo[i]
        # For each pixel bit, compute PF2 color and swap 1<->2
        new_h = 0
        new_l = 0
        for bit in range(16):
            mask_bit = (m >> (15-bit)) & 1
            hi_bit = (h >> (15-bit)) & 1
            lo_bit = (l >> (15-bit)) & 1
            if mask_bit == 0:
                # transparent - keep 0
                pass
            else:
                color = (lo_bit << 1) | hi_bit  # 0-3
                if color == 1:
                    color = 2
                elif color == 2:
                    color = 1
                # color 0 stays 0, color 3 stays 3
                new_hi = color & 1
                new_lo = (color >> 1) & 1
                if new_hi:
                    new_h |= (1 << (15-bit))
                if new_lo:
                    new_l |= (1 << (15-bit))
        invDataHi.append(new_h)
        invDataLo.append(new_l)
    return invDataHi, invDataLo

